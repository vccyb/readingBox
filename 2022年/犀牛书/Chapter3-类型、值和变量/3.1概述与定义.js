// JavaScript的对象类型是可修改的(mutable)，
// 而它的原始类 型是不可修改的(immutable)。
// 可修改类型的值可以改变，
// 比如 JavaScript程序可以修改对象属性和数组元素的值。
// 数值、布尔 值、符号、null和undefined是不可修改的，
// 以数值为例，修改它是 没有意义的。字符串可以看成字符数组，
// 你可能期望它们是可修改 的。但在JavaScript中，字符串也是不可修改的。
// 虽然可以按索引 访问字符串中的字符，但JavaScript没有提供任何方式去修改已有 字符串的字符。
// 可修改值与不可修改值的区别将在3.8节更详细地介 绍。